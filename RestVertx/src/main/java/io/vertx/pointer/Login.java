package io.vertx.pointer;

public class Login 
{
	private String _sUserName;
	private String _sUserPassword;
	
	public String get_sUserName() {
		return _sUserName;
	}
	public void set_sUserName(String _sUserName) {
		this._sUserName = _sUserName;
	}
	public String get_sUserPassword() {
		return _sUserPassword;
	}
	public void set_sUserPassword(String _sUserPassword) {
		this._sUserPassword = _sUserPassword;
	}
	
	
}
